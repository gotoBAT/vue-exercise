<template>
  <li>
    <label @change="handleCheck(todo.id)">
      <input type="checkbox" :checked="todo.done" />
      <span>{{ todo.title }}</span>
    </label>
    <button class="btn btn-danger" @click="handleDelete(todo.id)">删除</button>
  </li>
</template>

<script>
export default {
  name: "MyItem",
  props: ["todo", "deleteTodo", "checkOne"],
  methods: {
    handleDelete(id) {
      if (confirm("Are you sure you want to delete")) this.deleteTodo(id);
    },
    handleCheck(id) {
      this.checkOne(id);
    },
  },
};
</script>

